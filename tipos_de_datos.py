# Enteros y flotantes
x = 10 #Entero
y = 10.0 #Flotante
print(x)
print(y)

# Strings
a = 'Francisco'
b = "Francisco"
print(a)
print(b)

# Booleanos
t = True
f = False
print(t)
print(f)

# Listas
mi_lista = [10, 20, 30]
print(mi_lista)

# Tuples
mi_tuple = ('Meraki', 'Cisco', 'Todd', 2020)
print(mi_tuple)

# Diccionario
mi_diccionario = {'uno': 1, 'dos': 2}
print(mi_diccionario)

# Listas dentro de listas
mi_lista2 = [[10, 20, 30], ['Cisco Live', 'Junio', 2020]]
print(mi_lista2)